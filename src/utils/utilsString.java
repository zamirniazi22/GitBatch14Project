public class utilsString {

	public static void main(String[] args) {
		class StringUtils {


		    public void printAllIndexes(String str,char c) {
		        for(int i=0;i<str.length();i++) {

		            if(str.charAt(i)==c) {
		                System.out.println(i);
		            }
		        }
		    }
		}

	}

}
